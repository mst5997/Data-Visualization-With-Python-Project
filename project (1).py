"""16. Use PCA for 3 components"""

from sklearn.preprocessing import StandardScaler
from sklearn.decomposition import PCA
import pandas as pd

# Select the relevant numerical columns for PCA
pca_features = aggregated_df[['housing_cost', 'food_cost', 'transportation_cost', 'healthcare_cost', 'other_necessities_cost', 'childcare_cost', 'taxes', 'median_family_income']]

# Standardize the data
scaler = StandardScaler()
scaled_features = scaler.fit_transform(pca_features)

# Apply PCA with 3 components
pca = PCA(n_components=3)
pca_components = pca.fit_transform(scaled_features)

# Create a DataFrame for the PCA components
pca_df = pd.DataFrame(data = pca_components,
                      columns = ['PC1', 'PC2', 'PC3'])

# Add the state names back to the PCA DataFrame for identification
pca_df['state'] = aggregated_df['state']

# Display the explained variance ratio
print("Explained variance ratio per principal component:")
for i, exp_var in enumerate(pca.explained_variance_ratio_):
    print(f"PC{i+1}: {exp_var:.2f}")

print(f"\nTotal explained variance by 3 components: {pca.explained_variance_ratio_.sum():.2f}")

# Display the first few rows of the PCA DataFrame
print("\nPCA Components DataFrame:")
display(pca_df.head())

