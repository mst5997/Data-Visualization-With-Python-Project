from sklearn.decomposition import NMF
from sklearn.preprocessing import MinMaxScaler
import pandas as pd

# Select the relevant numerical columns for NMF (same as PCA features)
nmf_features = aggregated_df[['housing_cost', 'food_cost', 'transportation_cost', 'healthcare_cost', 'other_necessities_cost', 'childcare_cost', 'taxes', 'median_family_income']]

# Scale the data to a [0, 1] range to ensure non-negativity for NMF
scaler_nmf = MinMaxScaler()
scaled_nmf_features = scaler_nmf.fit_transform(nmf_features)

# Apply NMF with 3 components
nmf = NMF(n_components=3, random_state=42, init='random', max_iter=1000)
nmf_components = nmf.fit_transform(scaled_nmf_features)

# Create a DataFrame for the NMF components
nmf_df = pd.DataFrame(data = nmf_components,
                      columns = ['NMF1', 'NMF2', 'NMF3'])

# Add the state names back to the NMF DataFrame for identification
nmf_df['state'] = aggregated_df['state']

# Display the first few rows of the NMF DataFrame
print("NMF Components DataFrame:")
display(nmf_df.head())

# Optionally, display the feature loadings (components_ attribute)
print("\nNMF Feature Loadings (components_ attribute - weights for each original feature in each NMF component):")
display(pd.DataFrame(nmf.components_, columns=nmf_features.columns, index=['NMF1', 'NMF2', 'NMF3']))